let numero = 56;

// do = fazer ou faz
// while = enquanto
do {
  console.log(`número: ${numero}`);  // número: 51
  numero = numero - 1;               // numero = 51 - 1
} while(numero <= 55 && numero > 50);
//          true     &&     false       =   false

// primeiro FAZ e depois "faz de novo" ENQUANTO for verdadeiro