let numero = 50;  // 56

// loop ou laço
// iteração      false
for (let i = 0; i <= 5; i++) { // i = 6
  console.log(`i: ${i}`);            // i: 5
  console.log(`número: ${numero}`);  // número: 55
  console.log("\n");                 // (pula linha)
  numero = numero + 1;               //  numero = 55 + 1
}

console.log(`número: ${numero}`);    // número: 56
